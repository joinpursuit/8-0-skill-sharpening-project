/**
 * checkIfCharacterIsInString()
 * ---------------------
 * Checks if the given `character` appears in the `text`.
 * @param {string} text
 * @param {string} character
 * @returns {boolean}
 *
 * EXAMPLE:
 *  checkIfCharacterIsInString("hello there", "e");
 *  //> true
 *
 *  checkIfCharacterIsInString("hello there", "a");
 *  //> false
 */
function checkIfCharacterIsInString(text, character) {
  // if the character is included in the text...
  if (text.includes(character)) {
    //return the true
    return true;
  }
  //if else the char is NOT included in the text then...
  else {
    //return false
    return false;
  }
}

/**
 * isEven()
 * ---------------------
 * Returns `true` if the number given is even. Otherwise, returns false.
 * @param {number} num
 * @returns {boolean}
 *
 * EXAMPLE:
 *  isEven(10);
 *  //> true
 *
 *  isEven(11);
 *  //> false
 */
function isEven(num) {
  // if the the num is even then...
  if (num % 2 === 0) {
    //return true
    return true;
  }
  // if else...
  else {
    //return false
    return false;
  }
}

/**
 * isOdd()
 * ---------------------
 * Returns `true` if the number given is odd. Otherwise, returns `false`.
 * @param {number} num
 * @returns {boolean}
 *
 * EXAMPLE:
 *  isOdd(10);
 *  //> false
 *
 *  isOdd(11);
 *  //> true
 */
function isOdd(num) {
  //if the number is odd...
  if (num % 2 !== 0) {
    //return true
    return true;
  }
  //if else...
  else {
    //return false
    return false;
  }
}

