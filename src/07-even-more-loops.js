/**
 * sumArray()
 * ---------------------
 * Returns the sum of all values in the array.
 * @param {number[]} array
 * @returns {number}
 *
 * EXAMPLE:
 *  sumArray([ 10, 0, 10, 11 ]);
 *  //> 31
 */
function sumArray(array) {
  let totalSum = 0;
  for (let idx of array){
    totalSum += idx;
  } return totalSum
}

/**
 * oddArray()
 * ---------------------
 * Returns a new array of only the odd numbers from the original array.
 * @param {number[]} array
 * @returns {array}
 *
 * EXAMPLE:
 *  oddArray([ 11, 15, 20, 22, 37 ]);
 *  //> [ 11, 15, 37 ]
 */
function oddArray(array) {
  let newArray = new Array;
  let oddNumber
  for (let idx of array){
    if (idx % 2 === 1){
      oddNumber = idx;
      newArray.push(oddNumber);
    }
  } return newArray
}

/**
 * evenArray()
 * ---------------------
 * Returns a new array of only the even numbers from the original array.
 * @param {number[]} array
 * @returns {array}
 *
 * EXAMPLE:
 *  evenArray([ 11, 15, 20, 22, 37 ]);
 *  //> [ 20, 22 ]
 */
function evenArray(array) {
  let newArray = new Array;
  let evenNumber
  for (let idx of array){
    if (idx % 2 === 0){
      evenNumber = idx;
      newArray.push(evenNumber);
    }
  } return newArray
}

/**
 * findSmallest()
 * ---------------------
 * Returns the smallesÅ“t number from the array.
 * @param {number[]} array
 * @returns {number}
 *
 * EXAMPLE:
 *  findSmallest([ 0, 11, -2, 5 ]);
 *  //> -2
 */
function findSmallest(array) {
  let smallest = array[0]
  for (let idx = 0; idx < array.length; idx++){
    if(array[idx] < smallest){
      smallest = array[idx]
    }
  } return smallest
}


/**
 * findLargest()
 * ---------------------
 * Returns the largest number from the array.
 * @param {number[]} array
 * @returns {number}
 *
 * EXAMPLE:
 *  findLargest([ 0, 11, -2, 5 ]);
 *  //> 11
 */
function findLargest(array) {
  let biggest = array[0]
  for (let idx = 0; idx < array.length; idx++){
    if(array[idx] > biggest){
      biggest = array[idx];
    }
  } return biggest;
}

/**
 * findEqual()
 * ---------------------
 * Returns whether or not the `selected` value can be found in the array.
 * @param {number[]} array
 * @param {number} selected
 * @returns {boolean}
 *
 * EXAMPLE:
 *  findEqual([ 0, 11, -2, 5 ], 11);
 *  //> true
 *
 *  findEqual([ 0, 11, -2, 5 ], 9);
 *  //> false
 */
function findEqual(array, selected) {
  let numberMatch = false;
  for (let idx = 0; idx < array.length; idx++){
    if (array[idx] === selected){
      numberMatch = true;
    }
  } return numberMatch;
}

/**
 * removeDuplicates()
 * ---------------------
 * Returns a new array like the original array except there are no duplicates. The numbers in the array should be ordered similarly.
 *
 * HINT: The `.includes()` method could help here.
 *
 * @param {number[]} array
 * @returns {number[]}
 *
 * EXAMPLE:
 *  removeDuplicates([ 1, 11, 2, 3, 4, 4, 2, 11, 9 ]);
 *  //> [ 1, 11, 2, 3, 4, 9 ]
 */

 function removeDuplicates(array) {
  let newArray = new Array;
  for (let idx = 0; idx < array.length; idx++) {
    if (!newArray.includes(array[idx])) {
      newArray.push(array[idx])
    }
  } return newArray;
}

