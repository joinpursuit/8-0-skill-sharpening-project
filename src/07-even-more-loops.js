/**
 * sumArray()
 * ---------------------
 * Returns the sum of all values in the array.
 * @param {number[]} array
 * @returns {number}
 *
 * EXAMPLE:
 *  sumArray([ 10, 0, 10, 11 ]);
 *  //> 31
 */
function sumArray(array) {

  let sum = 0;
  for (const i of array) {
    sum += i
  }
  return sum
}

/**
 * oddArray()
 * ---------------------
 * Returns a new array of only the odd numbers from the original array.
 * @param {number[]} array
 * @returns {array}
 *
 * EXAMPLE:
 *  oddArray([ 11, 15, 20, 22, 37 ]);
 *  //> [ 11, 15, 37 ]
 */
function oddArray(array) {
  let newArr = [];
  for (const i of array) {
    if (i % 2 !== 0) {
      newArr.push(i)
    }
  }
  return newArr
} 
/**
 * evenArray()
 * ---------------------
 * Returns a new array of only the even numbers from the original array.
 * @param {number[]} array
 * @returns {array}
 *
 * EXAMPLE:
 *  evenArray([ 11, 15, 20, 22, 37 ]);
 *  //> [ 20, 22 ]
 */
function evenArray(array) {

  let newArr = [];
  for (const i of array) {
    if (i % 2 === 0) {
      newArr.push(i)
    }
  }
  return newArr

}

/**
 * findSmallest()
 * ---------------------
 * Returns the smallest number from the array.
 * @param {number[]} array
 * @returns {number}
 *
 * EXAMPLE:
 *  findSmallest([ 0, 11, -2, 5 ]);
 *  //> -2
 */
function findSmallest(array) {

  let compareNum = array[0]; // declare a variable and assign it the array's first index to be a starting point
  for (const i of array) {
    if (i < compareNum) {
      compareNum = i
    }
  }
    return compareNum
}

