/**
 * removeDuplicates()
 * ---------------------
 * Returns a new array like the original array except there are no duplicates. The numbers in the array should be ordered similarly.
 *
 * HINT: The `.includes()` method could help here.
 *
 * @param {number[]} array
 * @returns {number[]}
 *
 * EXAMPLE:
 *  removeDuplicates([ 1, 11, 2, 3, 4, 4, 2, 11, 9 ]);
 *  //> [ 1, 11, 2, 3, 4, 9 ]
 */

function removeDuplicates(array) {
  let duplicates = [];
  for(i=0;i < array.length; i++){
    if (!duplicates.includes(array[i])) {//if array does not include the index of array
      duplicates.push(array[i]);//push excluded indexs tothe duplicate array
    }
  }
  return duplicates
}

