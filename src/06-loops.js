/**
 * rangeOdd()
 * ---------------------
 * Takes in range (inclusive) and returns an array in decreasing order of only the odd numbers in that range.
 * @param {number} min
 * @param {number} max
 * @returns {number[]}
 *
 * EXAMPLE:
 *  rangeOdd(10, 15);
 *  //> [ 15, 13, 11 ]
 */
function rangeOdd(min, max) {

  let decreasingOdd = []

  for(let num = max; num >= min; num--){

    if(num % 2 !== 0){

      decreasingOdd.push(num)
    }
   
  }

  return decreasingOdd

}

